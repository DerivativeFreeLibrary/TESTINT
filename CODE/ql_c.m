function [y, g] = ql_c(x)
%-----------------------------------------
% Function ql
%	n = 2		m = 3
%	f(x*) = 7.2
%-----------------------------------------
	f(1) = x(1)^2 + x(2)^2;
	f(2) = x(1)^2 + x(2)^2 + 10.0*( - 4.0*x(1) - x(2) + 4.0 );
	f(3) = x(1)^2 + x(2)^2 + 10.0*( - x(1) - 2.0*x(2) + 6.0 );
    y = max(f);
    J = 1:(length(x)-1);
    g = x(J).^2 + x(J+1).^2 + x(J).*x(J+1) - 2*x(J) - 2*x(J+1) + 1;
end
