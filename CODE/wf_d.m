function [y, g] = wf_d(x)
%----------------------------
% Function WF
%----------------------------
    f(1) = ( x(1) + 10.0*x(1)/(x(1)+0.1) + 2.0*x(2)^2)/2.0;
    f(2) = (-x(1) + 10.0*x(1)/(x(1)+0.1) + 2.0*x(2)^2)/2.0;
    f(3) = ( x(1) - 10.0*x(1)/(x(1)+0.1) + 2.0*x(2)^2)/2.0;
    y = max(f);
    J = 1:(length(x)-1);
    g = x(J).^2 + x(J+1).^2 + x(J).*x(J+1) - 1;
end
