%-----------------------------------------
% Function Mifflin 2
%	n = 2		m = 2
%   f(x*) = -1
%-----------------------------------------
function y = mifflin2(x)
	temp = - x(1) + 2.0*(x(1)^2 + x(2)^2 - 1.0); 
	f(1) = temp + 1.75*(x(1)^2 + x(2)^2 - 1.0); 
	f(2) = temp - 1.75*(x(1)^2 + x(2)^2 - 1.0); 
    y = max(f);

end