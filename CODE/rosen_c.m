function [y, g] = rosen_c(x)
%-----------------------------------------
% Function rosen-suzuki
%	n = 4		m = 4
%	f(x*) = -44
%-----------------------------------------
	f(1) =  x(1)^2 + x(2)^2 + 2.0*x(3)^2 + x(4)^2; 
	f(1) =  f(1) - 5.0*( x(1) + x(2) ) - 21.0*x(3) + 7.0*x(4); 
	f(2) =  x(1)^2 + x(2)^2 + x(3)^2 + x(4)^2;
	f(2) =  f(2) + x(1) - x(2) + x(3) - x(4) - 8.0;
	f(3) =  x(1)^2 + 2.0*x(2)^2 + x(3)^2 + 2.0*x(4)^2;
	f(3) =  f(3) - x(1) - x(4) - 10.0;
	f(4) =  x(1)^2 + x(2)^2 + x(3)^2 + 2.0*x(1); 
	f(4) =  f(4) - x(2) - x(4) - 5.0;

	f(2) = 10.0*f(2) + f(1);
	f(3) = 10.0*f(3) + f(1);
	f(4) = 10.0*f(4) + f(1);
    
    y = max(f);

    J = 1:(length(x)-1);
    g = x(J).^2 + x(J+1).^2 + x(J).*x(J+1) - 2*x(J) - 2*x(J+1) + 1;
end
