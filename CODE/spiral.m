%---------------------------------
% Function SPIRAL
%---------------------------------
function y = spiral(x)
    f(1) = (x(1) - sqrt(x(1)^2+x(2)^2)*cos(sqrt(x(1)^2+x(2)^2)))^2 + 0.005*(x(1)^2+x(2)^2);
    f(2) = (x(2) - sqrt(x(1)^2+x(2)^2)*sin(sqrt(x(1)^2+x(2)^2)))^2 + 0.005*(x(1)^2+x(2)^2);
    y = max(f);
end