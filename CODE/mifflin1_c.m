function [y, g] = mifflin1_c(x)
%-----------------------------------------
% Function mifflin 1
%	n = 2		m = 2
%	f(x*) = -1
%-----------------------------------------
	f(1) = - x(1); 
	f(2) = - x(1) + (x(1)^2 + x(2)^2 - 1);
    y = max(f);
    J = 1:(length(x)-1);
    g = x(J).^2 + x(J+1).^2 + x(J).*x(J+1) - 2*x(J) - 2*x(J+1) + 1;
end
