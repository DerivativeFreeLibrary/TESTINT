%---------------------------------
% Function EVD52
%---------------------------------
function y = evd52(x)
    f(1) = sum(x.^2)-1.0;
    f(2) = x(1)^2+x(2)^2+(x(3)-2.0)^2;
    f(3) = sum(x) - 1.0;
    f(4) = x(1) + x(2) - x(3) + 1.0;
    f(5) = 2.0*x(1)^3 + 6.0*x(2)^2 + 2.0*(5.0*x(3) - x(1) + 1.0)^2;
    f(6) = x(1)^2 - 9.0*x(3);
    y = max(f);
end