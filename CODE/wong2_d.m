function [y, g] = wong2_d(X)
    F(1) = X(1)^2 + X(2)^2 + X(1)*X(2) - 14.0*X(1)- 16.0*X(2) + (X(3)-10)^2 + 4.0*(X(4)-5.0)^2 ...
         + (X(5)-3.0)^2 + 2.0*(X(6)-1.0)^2 + 5.0*X(7)^2 + 7.0*(X(8)-11.0)^2 ...
         + 2.0*(X(9)-10)^2 + (X(10)-7.0)^2 + 45.0;
    F(2) = F(1) + 10*(3.0*(X(1)-2.0)^2 + 4.0*(X(2)-3.0)^2 + 2.0*X(3)^2 -7.0*X(4)-120.0);
    F(3) = F(1) + 10*(5.0*X(1)^2 + 8.0*X(2) + (X(3)-6.0)^2 - 2.0*X(4) -40.0);
    F(4) = F(1) + 10*(0.5*(X(1)-8.0)^2 + 2.0*(X(2)-4.0)^2 + 3.0*X(5)^2 -X(6)-30.0);
    F(5) = F(1) + 10*(X(1)^2 + 2.0*(X(2)-2.0)^2 - 2.0*X(1)*X(2) + 14.0*X(5) - 6.0*X(6));
    F(6) = F(1) + 10*(-3.0*X(1) + 6.0*X(2) + 12.0*(X(9)-8.0)^2 - 7.0*X(10));
    F(7) = F(1) + 10*(4.0*X(1)+5.0*X(2)-3.0*X(7) +9.0*X(8)-105.0);
    F(8) = F(1) + 10*(10*X(1)-8.0*X(2)-17.0*X(7)+2.0*X(8));
    F(9) = F(1) + 10*(-8.0*X(1)+2.0*X(2)+5.0*X(9) -2.0*X(10)-12.0);
    y = max(F);
    J = 1:(length(X)-1);
    g = X(J).^2 + X(J+1).^2 + X(J).*X(J+1) - 1;
	return
end
